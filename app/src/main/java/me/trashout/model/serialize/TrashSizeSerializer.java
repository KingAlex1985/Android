package me.trashout.model.serialize;

import com.google.gson.JsonDeserializationContext;
import com.google.gson.JsonDeserializer;
import com.google.gson.JsonElement;
import com.google.gson.JsonNull;
import com.google.gson.JsonParseException;
import com.google.gson.JsonPrimitive;
import com.google.gson.JsonSerializationContext;
import com.google.gson.JsonSerializer;

import java.lang.reflect.Type;

import me.trashout.model.Constants;

public class TrashSizeSerializer implements JsonSerializer<Constants.TrashSize>, JsonDeserializer<Constants.TrashSize> {
    @Override
    public Constants.TrashSize deserialize(JsonElement json, Type typeOfT, JsonDeserializationContext context) throws JsonParseException {
        return Constants.TrashSize.getTrashSizeByName(json.getAsString());
    }

    @Override
    public JsonElement serialize(Constants.TrashSize src, Type typeOfSrc, JsonSerializationContext context) {
        if (src == null)
            return JsonNull.INSTANCE;
        return new JsonPrimitive(src.getName());
    }
}
